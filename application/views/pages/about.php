				<h4>HOW DOES IT WORK</h4>
				<p>By signing in to the site using their Facebook account, users are able to see a list of their friends and for each of those friends, 
				get recommendations based on the Facebook Pages related to books they already liked. Sounds tricky huh? Then I should not mention that 
				it uses an open knowledge database (Freebase) to disambiguate redundant entities and that it uses a Slope One algorithm based on your 
				closed graph of contacts to do the recommendation… but I will give you an example so you can figure out how it works:,/p>
				<p>Your friend Albert has liked the Facebook the pages “The Hobbit - the book” and “Alice in Wonderland”, so we might assume he has read 
				those two books.
Your other friends Bob and Claire also liked those two pages and they also liked “The Narnia Chronicles Book”, which Albert hasn’t. This means they have similar taste to Albert and that they have read a book Albert might enjoy reading too.
You get a recommendation to give Albert the book “Narnia Chronicles” as a gift.</p>
